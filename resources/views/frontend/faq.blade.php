		            <li>
		                <h3>What is FCCPC?</h3>
	                    <p></p>
	                    <p>
	                    	FCCPC is an acronym for Federal Competition and Consumer Protection Commission. FCCPC was established by Act no 66 of 1992 (now FCCPC Act Cap C25, LFN 2004) and commenced operations in 1999. FCCPC is an agency under the Federal Ministry of Industry, Trade and Investment (FMITI).
	                    </p>
	                    <p></p>
	                </li>
	                <li>
	                    <h3>Who is a consumer?</h3>
	                    <p></p>
	                    <p>
	                    	Section 32 of the Federal Competition and Consumer Protection Commission Act defines a consumer as “an individual, who purchases, uses, maintains or disposes of products or services”.
	                    </p>
	                    <p></p>
	                </li>
	                <li>
	                    <h3>What kinds of complaint can I bring to FCCPC?</h3>
	                    <p></p>
	                    <p>
	                    	FCCPC’s mandate allows it to intervene in any sector involving products and services. FCCPC defers to the sector specific regulators for resolution where appropriate. FCCPC does not handle criminal matters.
	                    </p>
	                    <p></p>
	                </li>
	                <li>
	                    <h3>How may I complain to FCCPC?</h3>
	                    <p></p>
	                    <p>
	                    	Any person who uses products or services expects to get value from them. When this expectation is not met, it gives rise to complaints.
	                    </p>
						<p>In order for FCCPC to address a complaint, the complainant must have initially engaged the provider of services or products. If not satisfied, the complainant can then file a complaint with FCCPC. This may either be in hard copy and delivered to any of our offices or by soft copy through the website portal or email. Please see the tab “About Us” on the home page.</p>
						<p>You can also give FCCPC tips on practices against the welfare of consumers. This can be done through our hotlines 08056002020 and 08056003030.</p>
						<p>The complaint must clearly state the following:</p>
						<ol>
						  <li>The party complained against, with the correct address</li>
						  <li>The amount involved</li>
						  <li>The expected redress</li>
						</ol>
						<p>You should also attach proof of transaction and any other document to support your claim.</p>
						<p>All these will help FCCPC with the process of redress, if a valid complaint is established.</p>
	                    <p></p>
	                </li>
	                <li>
	                    <h3>What is the time frame within which a complaint must be made after a breach of a right?</h3>
	                    <p></p>
	                        <p>A complaint must be made to the provider of products or services and then to FCCPC as soon as possible, after the right has been breached, especially within the warranty period.</p>
	                    <p></p>
	                </li>
	                <li>
	                    <h3>After lodging my complaint, how soon do I expect to hear from FCCPC?</h3>
	                    <p></p>
	                        <p>If your complaint was received electronically, you should expect an immediate acknowledgement. If your complaint was received in hard copy, you should expect to get an acknowledgment within 2 working days.</p>
	                    <p></p>
	                </li>
	                <li>
	                    <h3>What does it cost to make complaint?</h3>
	                    <p></p>
	                    	<p>It is completely free for a consumer to make a complaint.</p>
	                    <p></p>
	                </li>
	                <li>
	                    <h3>How long does it take to get redress for my complaint?</h3>
	                    <p></p>
		                    <p>A complaint could be resolved immediately or take much more time depending on the nature of the complaint. Complaints require a response from the provider of products and services which FCCPC will request for. Some require the intervention of other stakeholders such as sector regulators while others do not.  While FCCPC is committed to providing speedy redress to valid complaints, the provision of accurate information and documentation makes this easier and reduces the timelines. It takes anything between one (1) day and forty five days (45) days to get redress. However, some exceptions may exist beyond this timeframe.</p>
	                    <p></p>
	                </li>
	                <li>
	                    <h3>Can I still go to court in the event that I am dissatisfied with the redress I received from FCCPC?</h3>
	                    <p>
	                        </p><p>Whatever FCCPC does is without prejudice to the right of the consumer to go to court. In the event that the resolution offered by FCCPC does not satisfy the complainant, he/she may choose to proceed to court.</p>

	                    <p></p>
	                </li>
	                <li>
	                    <h3>Are there companies, organizations or persons that cannot be invited, summoned, ordered or prosecuted by the Commission where consumer rights are abused?</h3>
	                    <p>
	                        </p><p>No, except they are exempted by relevant laws.</p>

	                    <p></p>
	                </li>
	                <li>
	                    <h3>What steps does FCCPC take to ensure compliance of its Summons and Orders?</h3>
	                    <p>
	                        </p><p>In a few cases where there is disobedience, these are referred to the Office of the Attorney General for prosecution under Sections 18 and 21 of the FCCPCA. These Sections provide for fines and/or imprisonment or both.</p>

	                    <p></p>
	                </li>
	                <li>
	                    <h3>Does FCCPC collaborate with Non-Governmental-Organizations (NGOs) to enhance its operations?</h3>
	                    <p>
	                        </p><p>Yes. The Commission’s mandate allows it to encourage formation of voluntary consumer NGOs. It registers and collaborates with them in fighting against imperfections in the market place. NGOs are also deployed to sensitize consumers on their rights and responsibilities nationwide.</p>

	                    <p></p>
	                </li>
	                <li>
	                    <h3>How can I register an NGO with the Commission?</h3>
	                    <p>
	                        </p><p>Step 1: Get the approved guidelines for registration of NGO from the Commission.</p>

							<p>Step 2: Fill the registration form.</p>

							<p>Step 3: Pay the approved fee.</p>

	                    <p></p>
	                </li>
		            <li>
		                <h3>What should I do if I find unwanted particles in my drink?</h3>
		                <p>
		                    </p><p>Step 1: Complain to the seller of the drink.</p>

							<p>Step 2: If not satisfied, complain to the manufacturer via their contacts on the package of the drink.</p>

							<p>Step 3: if not satisfied, complain to FCCPC.</p>

		                <p></p>
		            </li>
		            <li>
		                <h3>How can I register a sales promotion?</h3>
		                <p>
		                    </p><p>Step 1. Download the sales promotion form from the Commission’s website and fill it as required.</p>

							<p>Step 2. Attach all required documentation to support the application.</p>

							<p>Step 3. Submit the terms and conditions guiding the promotion no later than 21 days before the commencement date of the draw.</p>

							<p>Please note that the Commission may grant provisional approval, where it is satisfied that the promotion is:</p>
							<ol>
							  <li>Legal, decent, honest and faithful</li>
							  <li>Not designed to abuse consumers’ trust or exploit their lack of knowledge or experience or mislead by ambiguity, exaggeration, omission or otherwise.</li>
							</ol>

							<p>Please refer to the tab For Businesses to receive further information.</p>

		                <p></p>
		            </li>
		            <li>
	                    <h3>What differentiates FCCPC from National Agency for Food &amp; Drug Administration &amp; Control (NAFDAC), Standards Organisation of Nigeria (SON) and other sector regulators?</h3>
	                    <p>
	                        </p><p>NAFDAC is a sector specific regulator for food, drugs, cosmetics, medical devices, chemicals and packaged water.</p>

							<p>SON is the body vested with the responsibility of standardizing and regulating the quality of all products in Nigeria.</p>

							<p>FCCPC is the foremost regulatory agency of the Federal Government, statutorily empowered to provide speedy redress to consumer complaints. Put succinctly, FCCPC stands on the demand side of the economy, while sector regulators operate from the supply side.</p>

	                    <p></p>
	                </li>
	                <li>
	                    <h3>How does FCCPC relate with the other sector regulators?</h3>
	                    <p>
	                        </p><p>FCCPC works in close collaboration with the standards body (SON) and sector regulators, such as NAFDAC, NCC, CBN, NCAA, NERC etc.  FCCPC enforces the standards and regulations set by these bodies.</p>

	                    <p></p>
	                </li>
	                <li>
	                    <h3>What is the difference between Public Complaints Commission (PCC) and FCCPC?</h3>
	                    <p>
	                        </p><p>Public Complaints Commission provides impartial investigation on behalf of complainants as a result of the action and/or inaction of government agencies or companies, while FCCPC provides speedy redress to consumers of products and services whose rights have been breached.</p>

	                    <p></p>
	                </li>
	                <li>
	                    <h3>Does FCCPC have the mandate to issue Summons to a government agency?</h3>
	                    <p>
	                        </p><p>Yes, if the government agency is providing a product or service that the consumer pays for.</p>

	                    <p></p>
	                </li>
	                <li>
	                    <h3>How does FCCPC assess the quality of products in the markets?</h3>
	                    <p>
	                        </p><p>FCCPC has laboratories and also uses third party laboratories to carry out routine laboratory tests of quality of products in the market. Results from these tests are compared with the requirements of the Nigerian Industrial Standards or other relevant international standards to check for conformity. Any product that does not conform is regarded as substandard and removed from the market.</p>

	                    <p></p>
	                </li>
	            </ul>
